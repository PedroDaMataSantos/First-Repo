    private void btLogActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btLogActionPerformed
         float numX = Float.parseFloat(tfPrimeiroNumero.getText());
    float baseY = Float.parseFloat(tfSegundoNumero.getText());
    if (numX <= 0) {
        lbResultado.setText("Erro: O logaritmando (primeiro número) deve ser positivo.");
        salvarOperacaoEmCSV("Logaritmo", String.valueOf(numX), String.valueOf(baseY), "Erro: Logaritmando inválido.");
        return;
    }
    if (baseY <= 0 || baseY == 1) {
        lbResultado.setText("Erro: A base (segundo número) deve ser positiva e diferente de 1.");
        salvarOperacaoEmCSV("Logaritmo", String.valueOf(numX), String.valueOf(baseY), "Erro: Base inválida.");
        return;
    }
    double resultado = Math.log(numX) / Math.log(baseY);
    lbResultado.setText(String.valueOf(resultado));
    salvarOperacaoEmCSV("Logaritmo", String.valueOf(numX), String.valueOf(baseY), String.valueOf(resultado));

    }//GEN-LAST:event_btLogActionPerformed

    private void salvarOperacaoEmCSV(String operacao, String primeiroNumero, String segundoNumero, String resultado) {
        String nomeArquivo = "historico_calculadora.csv";
        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("yyyy-MM-dd HH:mm:ss");
        String timestamp = LocalDateTime.now().format(formatter);

        try (FileWriter fw = new FileWriter(nomeArquivo, true); // '
                 PrintWriter pw = new PrintWriter(fw)) {

            pw.println(timestamp + "," + operacao + "," + primeiroNumero + "," + segundoNumero + "," + resultado);
            System.out.println("Operação salva em: " + nomeArquivo);
        } catch (IOException e) {
            System.err.println("Erro ao salvar operação no CSV: " + e.getMessage());

        }
    }

