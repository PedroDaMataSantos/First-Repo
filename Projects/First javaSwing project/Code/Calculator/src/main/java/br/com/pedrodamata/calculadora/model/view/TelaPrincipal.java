    private void btArcoSenoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btArcoSenoActionPerformed
        float valorSenoidal = Float.parseFloat(tfPrimeiroNumero.getText());
        if (valorSenoidal < -1 || valorSenoidal > 1) {
            lbResultado.setText("Erro: Valor inválido.");
            return;
        }
        float anguloEmRadianos = (float) Math.asin(valorSenoidal);
        float anguloEmGraus = (float) Math.toDegrees(anguloEmRadianos);
        lbResultado.setText(String.valueOf(anguloEmGraus));

        salvarOperacaoEmCSV("Arcoseno", String.valueOf(valorSenoidal), "", String.valueOf(anguloEmGraus));

    }//GEN-LAST:event_btArcoSenoActionPerformed

