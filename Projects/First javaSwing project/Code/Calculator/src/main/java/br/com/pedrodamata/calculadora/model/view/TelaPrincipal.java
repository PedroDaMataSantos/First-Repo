    private void btTangenteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btTangenteActionPerformed
        float angulo = Float.parseFloat(tfPrimeiroNumero.getText());
        if (Math.abs(angulo % 180 - 90) < 0.0001 || Math.abs(angulo % 180 + 90) < 0.0001) {
            lbResultado.setText("Erro: Tangente indefinida.");
            return;
        }

        double resultado = Math.tan(Math.toRadians(angulo));
        lbResultado.setText(String.valueOf(resultado));

        salvarOperacaoEmCSV("Tangente", String.valueOf(angulo), "", String.valueOf(resultado));

    }//GEN-LAST:event_btTangenteActionPerformed

