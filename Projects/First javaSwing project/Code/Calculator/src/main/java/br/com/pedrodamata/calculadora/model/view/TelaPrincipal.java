    private void btRaizActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btRaizActionPerformed
        float radicandoY = Float.parseFloat(tfPrimeiroNumero.getText());
        float indiceRaizX = Float.parseFloat(tfSegundoNumero.getText());

        if (indiceRaizX == 0) {
            lbResultado.setText("Erro: Índice não pode ser zero.");
            return;
        }

        if (radicandoY < 0 && indiceRaizX % 2 == 0) {
            lbResultado.setText("Erro: Raiz par de negativo.");
            return;
        }

        double resultado = Math.pow(radicandoY, 1.0 / indiceRaizX);
        lbResultado.setText(String.valueOf(resultado));

        salvarOperacaoEmCSV("Raiz", String.valueOf(radicandoY), String.valueOf(indiceRaizX), String.valueOf(resultado));


    }//GEN-LAST:event_btRaizActionPerformed

